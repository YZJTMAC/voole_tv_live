package com.gntv.tv.model.channel;

import java.io.Serializable;

public class ProgramItem implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -4616619983481348888L;
	private String programName;
	private String startTime;
	private String stopTime;
	private long lStartTime;
	private long lStopTime;
	private String mid;
	private String fid;
	private String sid;
	private boolean isPlaying = false;
	public String getProgramName() {
		return programName;
	}
	public void setProgramName(String programName) {
		this.programName = programName;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getStopTime() {
		return stopTime;
	}
	public void setStopTime(String stopTime) {
		this.stopTime = stopTime;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getFid() {
		return fid;
	}
	public void setFid(String fid) {
		this.fid = fid;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public boolean isPlaying() {
		return isPlaying;
	}
	public void setPlaying(boolean isPlaying) {
		this.isPlaying = isPlaying;
	}
	public long getlStartTime() {
		return lStartTime;
	}
	public void setlStartTime(long lStartTime) {
		this.lStartTime = lStartTime;
	}
	public long getlStopTime() {
		return lStopTime;
	}
	public void setlStopTime(long lStopTime) {
		this.lStopTime = lStopTime;
	}
	
}
