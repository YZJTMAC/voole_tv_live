package com.voole.android.client.messagelibrary.model;

import java.io.Serializable;
import java.util.List;

/**
 * 统计数据汇报类型 
 * @version V1.0
 * @author wusong
 * @time 2013-4-17上午10:33:41
 */
public class MessageArray implements Serializable{
	private static final long serialVersionUID = -881819318903770734L;
	
	private int count;
	
	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	private Header header;
	
	private List<Message> messageArray;
	
	public MessageArray(){}

	public Header getHeader() {
		return header;
	}

	public void setHeader(Header header) {
		this.header = header;
	}

	public List<Message> getMessageArray() {
		return messageArray;
	}

	public void setMessageArray(List<Message> messageArray) {
		this.messageArray = messageArray;
	}
	
	
}
