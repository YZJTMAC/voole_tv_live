package com.voole.statistics.bean;

/**
 * 播放消息bean
 * @author Jacky
 *
 */
public class PlayerMessageBean {
	
	/**
	 * 动作 
	 */
	private String action;
	
	/**
	 * fid 	介质文件ID
	 */
    private String fid;
    
    /**
     * 发生此事件时，节目的播放时长，单位:秒
     */
    private String playtime;
	
    /**
     * Seek的目标时长，单位:秒
     */
    private String seektime;
	 	
	 /**
	  * 	Apk的sessionid,需要与直播、点播的播放模块sessionid一致
	  */
	 private String  sessionid;

	 /**
	  * 	影片名称
	  */
	 private String videoName;
	 
	 /**
	  * 时长
	  */
	 private String duration;
	 
	 /**
	  * 错误码
	  */
	private String  errcode;
	 
	/**
	 * 信息描述
	 */
	private String info;
	
	/**
	 * 消息类别
	 */
	private int playerMessageType;
	
	/**
	 * 表示异常消息
	 */
	public static final int PLAYER_MESSAEG_TYPE_EXCETPION=1;
	
	/**
	 * 表示播放状态消息
	 */
	public static final int PLAYER_MESSAEG_TYPE_STATUS=2;
	
	/**
	 * 表示播放统计消息
	 */
	public static final int PLAYER_MESSAEG_TYPE_PLAYER=3;
	
	/**
	 * 启播统计信息类
	 */
	public static final int PLAYER_MESSAEG_TYPE_STARTPLAYER=4;
	 
	public String getErrcode() {
		return errcode;
	}

	public void setErrcode(String errcode) {
		this.errcode = errcode;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getVideoName() {
		return videoName;
	}

	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public String getFid() {
		return fid;
	}

	public void setFid(String fid) {
		this.fid = fid;
	}

	public String getPlaytime() {
		return playtime;
	}

	public void setPlaytime(String playtime) {
		this.playtime = playtime;
	}

	public String getSeektime() {
		return seektime;
	}

	public void setSeektime(String seektime) {
		this.seektime = seektime;
	}

	public String getSessionid() {
		return sessionid;
	}

	public void setSessionid(String sessionid) {
		this.sessionid = sessionid;
	}

	public int getPlayerMessageType() {
		return playerMessageType;
	}

	public void setPlayerMessageType(int playerMessageType) {
		this.playerMessageType = playerMessageType;
	}
	 
	 
}
